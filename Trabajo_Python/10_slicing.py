nombre = 'Carolina Gomez Camelo'
# INDEXING:
# Posición índice de 'C' es cero:
print(nombre[0])
print(nombre[4])
# SLICING:
print(nombre[1:4])  # nombre [1:3+1]se imprime del 1 al 3
print(nombre[2:7])
print(nombre[7:12])
print(nombre[0:14])
print(nombre[0:])
# Crln
print(nombre[0:21])
print(nombre[0:21:2])
print(nombre[0:21:3])
# Regresivo:
print('Regresivo:')
print(nombre[-1:-9:-1])
