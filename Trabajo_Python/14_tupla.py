# crear tuplas (no es mutable o modificable):
num1 = (1, 2, 3, 4, 5)
print(num1)
print(type(num1))
num2 = ("a", True, 3.14, 12)
print(num2)
print(type(num2))
num3 = ("a", [1, 2], False)
print(num3)
print(type(num3))
print(num3[1])
num4 = ["a", True, (1, 2)]
print(num4[2])
print(type(num4))
print(len(num1))  #para ver el tamaÃ±o o la longitud de una tupla
print(list(num1))
